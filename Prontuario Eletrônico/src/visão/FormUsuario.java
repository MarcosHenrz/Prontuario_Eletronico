    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
       int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir??", "Confirma Exclusão", JOptionPane.YES_NO_OPTION);
        
       if (resposta == 0) {
        BeansUsuario usu = new BeansUsuario();
        usu.setCodUsuario(Integer.parseInt(jTextFieldCod.getText()));
       
        DaoUsuario usuDao = new DaoUsuario();
        usuDao.excluir(usu);
        JOptionPane.showMessageDialog(this, "Deletado");
       }
       listarTabela();
       
       limparCampos();
       
       
    }//GEN-LAST:event_jButtonExcluirActionPerformed

