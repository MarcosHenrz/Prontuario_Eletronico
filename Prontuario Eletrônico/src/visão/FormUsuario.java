    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        BeansUsuario usu = new BeansUsuario();
        usu.setCodUsuario(Integer.parseInt(jTextFieldCod.getText()));
        usu.setNomeUsuario(jTextFieldUsuario.getText());
        usu.setSenhaUsuario(jPasswordFieldSenha.getText());
        usu.setTipo((String) jComboBoxTipo.getSelectedItem());
       
        DaoUsuario usuDao = new DaoUsuario();
        usuDao.salvar(usu);
        JOptionPane.showMessageDialog(this, "Salvo");
        
        listarTabela();
        limparCampos();
            
    }//GEN-LAST:event_jButtonSalvarActionPerformed

