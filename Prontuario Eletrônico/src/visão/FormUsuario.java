    public void listarTabela(){
      
       DefaultTableModel val = (DefaultTableModel) jTableUsuarios.getModel();
       val.getDataVector().removeAllElements();
       DaoUsuario usuDao = new DaoUsuario();
       List <BeansUsuario> usuario = usuDao.listarUsuarios();
       int i = 0;
       while(usuario.size() > i){
           
       val.addRow(new Object[] {usuario.get(i).getCodUsuario(),  usuario.get(i).getNomeUsuario(), usuario.get(i).getSenhaUsuario(), usuario.get(i).getTipo()});
       i++;
       }
    } 
    
    public void listarTabelaNomes(String nome){
      
       DefaultTableModel val = (DefaultTableModel) jTableUsuarios.getModel();
       val.getDataVector().removeAllElements();
       DaoUsuario usuDao = new DaoUsuario();
       List <BeansUsuario> usuario = usuDao.pesquisarUsuarios(nome);
       int i = 0;
       while(usuario.size() > i){
           
       val.addRow(new Object[] {usuario.get(i).getCodUsuario(),  usuario.get(i).getNomeUsuario(), usuario.get(i).getSenhaUsuario(), usuario.get(i).getTipo()});
       i++;
       }
    }
    
    public void CarregarCampos(){
        int setar = jTableUsuarios.getSelectedRow();
        
        jTextFieldCod.setText(jTableUsuarios.getModel().getValueAt(setar, 0).toString());
        jTextFieldUsuario.setText(jTableUsuarios.getModel().getValueAt(setar, 1).toString());
        jPasswordFieldSenha.setText(jTableUsuarios.getModel().getValueAt(setar, 2).toString());
        jComboBoxTipo.setSelectedItem(jTableUsuarios.getModel().getValueAt(setar, 3).toString());
            
    }
     public void limparCampos(){
        jTextFieldCod.setText("");
        jTextFieldUsuario.setText("");
        jPasswordFieldSenha.setText("");
        jComboBoxTipo.setSelectedItem("Selecione");
    } 
     
     
