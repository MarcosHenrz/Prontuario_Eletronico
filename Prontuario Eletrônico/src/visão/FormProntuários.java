    public void listarTabelaNome(String nome){
      
       DefaultTableModel val = (DefaultTableModel) jTableProntuario.getModel();
       val.getDataVector().removeAllElements();
       DaoProntuarios prontDao = new DaoProntuarios();
       List <BeansProntuario> prontuario = prontDao.pesquisarProntuarios(nome);
       int i = 0;
       while(prontuario.size() > i){
           
       val.addRow(new Object[] {prontuario.get(i).getCodProntuario(), prontuario.get(i).getNomePaciente(), prontuario.get(i).getDiagnostico(), prontuario.get(i).getExame(),prontuario.get(i).getReceita(), prontuario.get(i).getSituaçao(), prontuario.get(i).getLocalização()});
       i++;
       }
    }    
    
    public void listarTabela(){
      
       DefaultTableModel val = (DefaultTableModel) jTableProntuario.getModel();
       val.getDataVector().removeAllElements();
       DaoProntuarios prontDao = new DaoProntuarios();
       List <BeansProntuario> prontuario = prontDao.listarProntuarios();
       int i = 0;
       while(prontuario.size() > i){
           
       val.addRow(new Object[] {prontuario.get(i).getCodProntuario(), prontuario.get(i).getNomePaciente(), prontuario.get(i).getDiagnostico(), prontuario.get(i).getExame(),prontuario.get(i).getReceita(), prontuario.get(i).getSituaçao(), prontuario.get(i).getLocalização()});
       i++;
       }
    } 
    
    public void limparCampos(){
        jTextFieldCodProntuario.setText("");
        jTextFieldPaciente.setText("");
        jTextFieldDiagnostico.setText("");
        jTextFieldExames.setText("");
        jTextFieldReceita.setText("");
        jComboBoxSituaçao.setSelectedItem("Selecione");
        jTextFieldLocalizaçao.setText("");
    }
    
    public void CarregarCampos(){
        int setar = jTableProntuario.getSelectedRow();
        
        jTextFieldCodProntuario.setText(jTableProntuario.getModel().getValueAt(setar, 0).toString());
        jTextFieldPaciente.setText(jTableProntuario.getModel().getValueAt(setar, 1).toString());
        jTextFieldDiagnostico.setText(jTableProntuario.getModel().getValueAt(setar, 2).toString());
        jTextFieldExames.setText(jTableProntuario.getModel().getValueAt(setar, 3).toString());
        jTextFieldReceita.setText(jTableProntuario.getModel().getValueAt(setar, 4).toString());
        jComboBoxSituaçao.setSelectedItem(jTableProntuario.getModel().getValueAt(setar, 5).toString());
        jTextFieldLocalizaçao.setText(jTableProntuario.getModel().getValueAt(setar, 6).toString());
    }
    
