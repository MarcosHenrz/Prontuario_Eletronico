    public void listarTabelaConsultas(){
      
       DefaultTableModel val = (DefaultTableModel) jTableConsultas.getModel();
       val.getDataVector().removeAllElements();
       DaoConsulta conDao = new DaoConsulta();
       List <BeansConsulta> usuario = conDao.listarConsultas();
       int i = 0;
       while(usuario.size() > i){
           
       val.addRow(new Object[] {usuario.get(i).getCodConsulta(),  usuario.get(i).getNomePaciente(), usuario.get(i).getMedico(), usuario.get(i).getData(), usuario.get(i).getHora()});
       i++;
       }
    } 
    
    public void listarTabelaNomes(String nome){
      
       DefaultTableModel val = (DefaultTableModel) jTableConsultas.getModel();
       val.getDataVector().removeAllElements();
       DaoConsulta conDao = new DaoConsulta();
       List <BeansConsulta> usuario = conDao.pesquisarConsultas(nome);
       int i = 0;
       while(usuario.size() > i){
           
       val.addRow(new Object[] {usuario.get(i).getCodConsulta(),  usuario.get(i).getNomePaciente(), usuario.get(i).getMedico(), usuario.get(i).getData(), usuario.get(i).getHora()});
       i++;
       }
    } 
    
    
