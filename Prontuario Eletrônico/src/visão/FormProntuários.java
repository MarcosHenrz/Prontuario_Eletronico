    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
       int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir??", "Confirma Exclusão", JOptionPane.YES_NO_OPTION);
       
       if (resposta == 0){
        BeansProntuario pront = new BeansProntuario();
        pront.setCodProntuario(Integer.parseInt(jTextFieldCodProntuario.getText()));
        
        DaoProntuarios prontDao = new DaoProntuarios();
        prontDao.excluir(pront);
        JOptionPane.showMessageDialog(this, "Excluido");
       }
        
        listarTabela();
        limparCampos();
    }//GEN-LAST:event_ExcluirActionPerformed

