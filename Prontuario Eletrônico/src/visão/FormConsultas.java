    public void limparCampos(){
        
        jTextFieldCodConsulta.setText("");
        jTextFieldPaciente.setText("");
        jComboBoxMedico.setSelectedItem("Selecione");
        jTextFieldData.setText("");
        jTextFieldHora.setText("");
        
    }
    
    public void carregarCampos(){
        int setar = jTableConsultas.getSelectedRow();
        
        jTextFieldCodConsulta.setText(jTableConsultas.getModel().getValueAt(setar, 0).toString());
        jTextFieldPaciente.setText(jTableConsultas.getModel().getValueAt(setar, 1).toString());
        jComboBoxMedico.setSelectedItem(jTableConsultas.getModel().getValueAt(setar, 2).toString());
        jTextFieldData.setText(jTableConsultas.getModel().getValueAt(setar, 3).toString());
        jTextFieldHora.setText(jTableConsultas.getModel().getValueAt(setar, 4).toString());
    }
